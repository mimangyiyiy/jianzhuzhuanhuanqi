class SensitiveFilter:
    def __init__(self):
        self.patterns = []
        for r in BUILTIN_RULES:
            try:
                self.patterns.append(re.compile(r, re.IGNORECASE))
            except re.error:
                pass

    def contains(self, text):
        if not text or not self.patterns:
            return False
        for rx in self.patterns:
            if rx.search(text):
                return True
        return False

    def count(self):
        return len(self.patterns)
